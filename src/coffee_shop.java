    private void confirm_bttnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirm_bttnMouseClicked
         
        String amountString = display_amount.getText();
        
        if (amountString.equals("") || amountString.equals("0")){
         JOptionPane.showMessageDialog(rootPane, "YOUR CART IS EMPTY, PLEASE PLACE AN ORDER.");
         
        } else {
            
         DefaultTableModel dtm = (DefaultTableModel) order_table.getModel();
        
         try{
             Properties props = new Properties();
            String driver = props.getProperty("jdbc.driver");
            if (driver != null) {
                Class.forName(driver);
            }
            Properties prop = new Properties();
            prop.put("user", "root");
            prop.put("password", "");
            Connection con = null;
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kapeng_barato_db", prop);
            System.out.println("Connected to database!");
            
            int rows = dtm.getRowCount();
             boolean allSizesCorrect = true;

             // Check coffee sizes
             for (int j = 0; j < rows; j++) {
                 Object table_coffee_size = dtm.getValueAt(j, 2);

                 if (!(table_coffee_size.equals("16 oz") || table_coffee_size.equals("24 oz"))) {
                     JOptionPane.showMessageDialog(rootPane, "Available sizes are 16 oz and 24 oz \n Please input the correct size at row "+ (j+1));
                     allSizesCorrect = false;
                 }
             }

             // Update total amounts based on size
             for (int i = 0; i < dtm.getRowCount(); i++) {
                 int quant = (int) dtm.getValueAt(i, 1);
                 Object size = dtm.getValueAt(i, 2);
                 double total_amount = (double) dtm.getValueAt(i, 3);

                 if (size.equals("16 oz")) {
                     total_amount = quant * 39.00;
                 } else if (size.equals("24 oz")) {
                     total_amount = quant * 49.00;
                 }

                 dtm.setValueAt(total_amount, i, 3);
             }

             updateTotalAmount();

             if (allSizesCorrect) {
                 
                 if (cash.getText().isEmpty()) {
                     
                     int confirmation = JOptionPane.showConfirmDialog(rootPane, "You cannot make changes anymore, continue to confirm order?");

                     if (confirmation == JOptionPane.YES_OPTION || !order_table.isEnabled()) {
                         order_table.clearSelection();
                     } else {
                         JOptionPane.showMessageDialog(rootPane, "Action Canceled.");
                         return; 
                     }
                 }

                 try{
                     float checkcash = Float.parseFloat(String.valueOf(cash.getText()));
                     String total_amount = display_amount.getText();
                     float total_amount_f = Float.parseFloat(total_amount);
                     
                     if (checkcash < total_amount_f) {
                         JOptionPane.showMessageDialog(rootPane, "Insufficient cash amount. Please enter a sufficient amount.");
                         return; 
                     } else {
                         float change_ = checkcash - total_amount_f;
                        change.setText(String.valueOf(change_));
                     }
                     
                 } catch (NumberFormatException e) {
                     JOptionPane.showMessageDialog(rootPane, "Please enter only numbers.");
                     return;
                  }

                 
                 int confirm = JOptionPane.showConfirmDialog(rootPane, "Confirm order?");
                 if (confirm == JOptionPane.YES_OPTION) {
                     // Insert data into the database
                     LocalDate current_date = LocalDate.now();
                     LocalDateTime time_order = LocalDateTime.now();
                     DateTimeFormatter format_time = DateTimeFormatter.ofPattern("HH:mm:ss");
                     String formattedTime = time_order.format(format_time);
                     
                     String insertQuery = "INSERT INTO orders (date,time, coffee, order_quantity,coffee_size, total) VALUES(?,?,?,?,?,?)";
                     
                     PreparedStatement ps;
                     ps = (PreparedStatement) con.prepareStatement(insertQuery);
                     
                     receipt recent_order_receipt = new receipt();
                     StringBuilder ordersText = new StringBuilder(),qty = new StringBuilder();
                     
                      for (int i = 0; i < rows; i++) {
                                          
                                          
                                                Object coffee_name = dtm.getValueAt(i,0);
                                                Object order_quantity = dtm.getValueAt(i,1);
                                                Object size = dtm.getValueAt(i,2);
                                                Object total = dtm.getValueAt(i,3);
                                                    
                                                ps.setString(1,String.valueOf(current_date));
                                                ps.setString(2,String.valueOf(formattedTime));
                                                ps.setString(3,String.valueOf(coffee_name));
                                                ps.setString(4, String.valueOf(order_quantity));
                                                ps.setString(5, String.valueOf(size));
                                                ps.setString(6, String.valueOf(total));
                                                
                                                ps.execute();
                   
                   
                  ordersText.append(String.valueOf(coffee_name)).append(" ").append(String.valueOf(size)).append("\n");
                  qty.append("  ").append(String.valueOf(order_quantity)).append("\t  ").append(String.valueOf(total)).append("\n");  
                  
                      }
                      
                      recent_order_receipt._date_.setText(String.valueOf(String.valueOf(current_date)));
                      recent_order_receipt.order_time.setText(String.valueOf(formattedTime));
                      recent_order_receipt.display_orders.append(ordersText.toString());
                      recent_order_receipt.qty_amount.append(qty.toString());
                      recent_order_receipt.total.setText("Total:");
                      recent_order_receipt.total_amnt.setText(display_amount.getText());                  
                      recent_order_receipt.cash.setText("Cash:");
                      recent_order_receipt.cash_amnt.setText(String.valueOf(cash.getText()));     
                      recent_order_receipt.cashback.setText("Change back (Cash):");
                      recent_order_receipt.cashback_amnt.setText(String.valueOf(change.getText()));     
                      recent_order_receipt.setVisible(true);
                      
                      
        
                     System.out.println("Added to database");
                     clear(); // Clear any necessary fields after successful confirmation
                 } else {
                     JOptionPane.showMessageDialog(rootPane, "Action Canceled.");
                 }
             }

                                     
                 } catch (Exception e) {
                      System.err.println(e.getMessage());
                                      }
         
        }
    }//GEN-LAST:event_confirm_bttnMouseClicked

